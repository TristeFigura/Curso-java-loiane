package curso.java.aula34;

public class ConversaoDeUnidadesDeArea {
	
	
	public static double converterMetro2_Pe2(double metroQuad) {
		return metroQuad*10.76;
	}
	
	public static double converterPe2_Cm2(double pe2) {
		return pe2*929;
	}
	
	public static double converterMilha2_Acre(double milha2) {
		return milha2*640;
	}

	public static double converterAcre_Pe2(double acre) {
		return acre*43560;
	}
}
